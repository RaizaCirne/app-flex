console.log("Conversão de Tipos");

console.log("ano" + 2020);
console.log("2" + "2"); // concatenação
console.log(parseInt("2") + parseInt("2")); // converte string para número

console.log("7" / "2"); // no caso de divisão não precisamos usar o parseInt
console.log("Ricardo" / "2"); // NaN - Not a Number

console.log(6.5); // ponto flutuante para trabalhar com números
console.log(6, 5); // não utilizar vírgula
